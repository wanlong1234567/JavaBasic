public class HelloWorld {
    //主方法，为程序的入口
    /**程序的执行从main开始，main结束则程序结束*/
    public static void main(String[] args) {
            //输出HelloWorld
            //1)严格区分大小写
            //2)所有符号必须是英文模式的
            //3)每句话必须以分号结尾
            //4)println():输出并换行
            //  print():输出不换行
            System.out.print("Hello");
            System.out.println("World");
            System.out.println("今天天气不错，心情也不错");
            System.out.println("欢迎大家来到达内");
    }
}
